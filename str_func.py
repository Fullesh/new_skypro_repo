def capital_str(string):
    """
    Получает на вход переменную string и преобразует все буквы в строке в заглавные
    """
    return string.capitalize()


def first_capital(string):
    """
    Получает на вход переменную string и преобразует первый символ в строке в заглавную букву
    """
    return string[0].capitalize()